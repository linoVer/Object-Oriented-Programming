package ex4;

public class Computador {
	
	private String memoria;
	private String hd;
	private String preco;
	private int tela;
	private String processador;
	
	public Computador(String memoria, String hd, String preco, int tela,
			String processador) {		
		this.memoria = memoria;
		this.hd = hd;
		this.preco = preco;
		this.tela = tela;
		this.processador = processador;
	}
	
	

}
